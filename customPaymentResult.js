/*
 * paymentResult.html 修改摘要
 * 
 * 此文件记录了对paymentResult.html的主要修改，以确保其符合移动浏览器兼容性
 * 并使用内联翻译系统而不是依赖外部的sharedLanguage.js文件。
 */

// 主要更改包括：

// 1. 添加了内联翻译数据和功能
//    - 添加了英语和泰语的翻译字典对象
//    - 实现了getCurrentLanguage()、translate()、setLanguage()函数
//    - 添加了updateTime()函数更新状态栏时间
//    - 添加了与旧代码兼容的getTranslation()函数

// 2. 移除了对外部JS文件的依赖
//    - 删除了对sharedLanguage.js的引用
//    - 内联实现了所有必要的翻译和语言管理功能

// 3. 图片路径优化
//    - 将所有图片路径从"./sppImages/"改为"sppImages/"以提高移动兼容性
//    - 涉及的图片包括:
//      - 状态栏的wifi和电池图标
//      - 成功图标和QR码图标
//      - Philip Morris Logo

// 4. 添加了DOM变化监听器
//    - 使用MutationObserver监听DOM变化
//    - 当有新元素添加时自动应用翻译
//    - 确保动态添加的内容也能被正确翻译

// 5. 页面初始化逻辑优化
//    - 在DOMContentLoaded事件开始时初始化语言和时间
//    - 添加时间自动更新的定时器

// 这些修改确保了:
// - 页面可以在没有外部语言文件的情况下正常运行
// - 所有文本内容可以根据用户选择的语言自动切换
// - 图片路径符合移动浏览器的要求
// - 动态内容也能够正确显示翻译后的文本

